package dreamschool01.cscourse.chapter05;

public class GuguDanTest {

	public static void main(String[] args) {
		for(int dan = 2; dan <= 9; dan++) {
			for(int i = 1; i <= 9; i++) {
				System.out.print(dan + " X " + i + " = " + dan  * i + "\t");
				
			}
			System.out.println();
		}

	}

}
