package dreamschool01.cscourse.chapter05;

public class Gugu3dan {

	public static void main(String[] args) {
		int dan3 = 3;
		int dan7 = 7;
		for(int i = 1; i < 10; i++) {
			System.out.print(dan3 + " X " + i + " = " + (dan3 * i) + "\t");
		}
		System.out.println("");
		
		for(int j = 1; j < 10; j++) {
			System.out.print(dan7 + " X " + j + " = " + (dan7 * j) + "\t");
		}
	}

}
